#include "main2.h"

/**
 * add - function to add
 * @a: int
 * @b: int
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub  - function to sub
 * @a: int
 * @b: int
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - function to multiply
 * @a: int
 * @b: int
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - function to div
 * @a: int
 * @b: int
 * Return: int
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - function to modul
 * @a: int
 * @b: int
 * Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
