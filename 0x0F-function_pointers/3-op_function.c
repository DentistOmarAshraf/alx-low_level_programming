#include "calc.h"
/**
 * op_add - add 2 int
 * @a: int
 * @b: int
 * Return: return sum of a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub 2 int
 * @a: int
 * @b: int
 * Return: sub of a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply 2 int
 * @a: int
 * @b: int
 * Return: multiplaction of 2 int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - devide numbers
 * @a: int
 * @b: int
 * Return: devision of 2 numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return reminder of dev
 * @a: int
 * @b: int
 * Return: reminder of a / b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
